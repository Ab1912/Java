public class math_functions {
    public static void main(String[] args) {
        System.out.println("Absolute number : "+Math.abs(-12));
        System.out.println("Round value : "+Math.round(2.588));
        System.out.println("Ceiling value : "+Math.ceil(2.588));
        System.out.println("Floor value : "+Math.floor(2.588));

        double a = 2, b = 3;
        System.out.println("Maximum of a and b is : "+Math.max(a, b));
        System.out.println("Minimum of a and b is : "+Math.min(a, b));
        System.out.println("Square root of a is : "+Math.sqrt(a));
        System.out.println("Square of a to the b is : "+Math.pow(a, b));
        System.out.println("Logarithm of a is : "+Math.log(a));
        System.out.println("Log10 of a is : "+Math.log10(a));
        System.out.println("Sin of a : "+Math.sin(a));
        System.out.println("Cos of a : "+Math.cos(a));
        System.out.println("Tan of a : "+Math.tan(a));
    }
}
